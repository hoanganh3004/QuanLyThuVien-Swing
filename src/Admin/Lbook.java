    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        // Lấy chỉ số dòng được chọn trong bảng
        int selectedRow = jTable1.getSelectedRow();
        // Kiểm tra xem có dòng nào được chọn không
        if (selectedRow != -1) {
            // Lấy ID danh mục từ cột 0
            int id = (int) jTable1.getValueAt(selectedRow, 0);
            // Lấy tên danh mục từ jTextField2
            String name = jTextField2.getText().trim();
            // Lấy mô tả từ jTextArea1
            String description = jTextArea1.getText().trim();

            // Kiểm tra xem tên danh mục có rỗng không
            if (name.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập tên loại!");
                return;
            }

            try {
                // Câu lệnh SQL cập nhật tên và mô tả danh mục
                String sql = "UPDATE categories SET name = ?, description = ? WHERE id = ?";
                PreparedStatement ps = conn.prepareStatement(sql);
                ps.setString(1, name);
                // Nếu mô tả rỗng, đặt giá trị null
                ps.setString(2, description.isEmpty() ? null : description);
                ps.setInt(3, id);
                // Thực thi câu lệnh cập nhật
                int rowsAffected = ps.executeUpdate();
                // Kiểm tra kết quả cập nhật
                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(this, "Cập nhật loại sách thành công!");
                    loadCategories(); // Làm mới bảng danh mục
                } else {
                    JOptionPane.showMessageDialog(this, "Cập nhật thất bại!");
                }
            } catch (SQLException e) {
                // Ghi log lỗi để debug
                e.printStackTrace();
                // Hiển thị thông báo lỗi
                JOptionPane.showMessageDialog(this, "Lỗi khi cập nhật: " + e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một loại sách để cập nhật!");
        }
    }//GEN-LAST:event_jButton11ActionPerformed

    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        String name = jTextField2.getText().trim();
        // Lấy mô tả từ jTextArea1
        String description = jTextArea1.getText().trim();

        // Kiểm tra xem tên danh mục có rỗng không
        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên loại!");
            return;
        }

        try {
            // Câu lệnh SQL thêm danh mục mới
            String sql = "INSERT INTO categories (name, description) VALUES (?, ?)";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, name);
            // Nếu mô tả rỗng, đặt giá trị null
            ps.setString(2, description.isEmpty() ? null : description);
            // Thực thi câu lệnh thêm
            int rowsAffected = ps.executeUpdate();
            // Kiểm tra kết quả thêm
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Thêm loại sách thành công!");
                // Xóa nội dung nhập liệu
                jTextField2.setText("");
                jTextArea1.setText("");
                loadCategories(); // Làm mới bảng danh mục
            } else {
                JOptionPane.showMessageDialog(this, "Thêm thất bại!");
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi khi thêm: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton7ActionPerformed

