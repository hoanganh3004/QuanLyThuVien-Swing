    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
            // Lấy trạng thái được chọn từ jComboBox1
            String selectedStatus = jComboBox1.getSelectedItem().toString();
            // Lấy thông tin từ các JLabel để xác định giao dịch
            String fullName = jLabel3.getText();
            String bookTitle = jLabel13.getText();
            String borrowDate = jLabel15.getText();

            try {
                // Câu lệnh SQL cập nhật trạng thái giao dịch
                String sql = "UPDATE borrowings b " +
                             "JOIN users u ON b.user_id = u.id " +
                             "JOIN books bk ON b.book_id = bk.id " +
                             "SET b.status = ? " +
                             "WHERE u.full_name = ? AND bk.title = ? AND b.borrow_date = ?";
                // Chuẩn bị câu lệnh SQL với các tham số
                PreparedStatement ps = conn.prepareStatement(sql);
                ps.setString(1, selectedStatus);
                ps.setString(2, fullName);
                ps.setString(3, bookTitle);
                ps.setString(4, borrowDate);

                // Thực thi câu lệnh cập nhật
                int rowsUpdated = ps.executeUpdate();
                // Kiểm tra xem có dòng nào được cập nhật không
                if (rowsUpdated > 0) {
                    JOptionPane.showMessageDialog(this, "Cập nhật trạng thái thành công!");
                } else {
                    JOptionPane.showMessageDialog(this, "Không tìm thấy giao dịch để cập nhật!");
                }
            } catch (SQLException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Lỗi khi cập nhật trạng thái: " + e.getMessage());
            }
    }//GEN-LAST:event_jButton8ActionPerformed

