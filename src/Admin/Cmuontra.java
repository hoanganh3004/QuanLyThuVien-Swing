    private void loadBorrowingDetails(String fullName, String bookTitle, String borrowDate) {
        try {
            String sql = "SELECT u.full_name, u.email, bk.title, bk.isbn, b.borrow_date, b.return_date, b.actual_return_date, b.status " +
                         "FROM borrowings b " +
                         "JOIN users u ON b.user_id = u.id " +
                         "JOIN books bk ON b.book_id = bk.id " +
                         "WHERE u.full_name = ? AND bk.title = ? AND b.borrow_date = ?";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, fullName);
            ps.setString(2, bookTitle);
            ps.setString(3, borrowDate);
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                jLabel3.setText(rs.getString("full_name"));
                jLabel12.setText(rs.getString("email"));
                jLabel13.setText(rs.getString("title"));
                jLabel14.setText(rs.getString("isbn")); 
                jLabel15.setText(rs.getString("borrow_date"));
                jLabel16.setText(rs.getString("return_date") != null ? rs.getString("return_date") : "Chưa xác định");  
                jLabel17.setText(rs.getString("actual_return_date") != null ? rs.getString("actual_return_date") : "Chưa trả");
                jComboBox1.setSelectedItem(rs.getString("status") != null ? rs.getString("status") : "Không xác định");
            } else {
                JOptionPane.showMessageDialog(this, "Không tìm thấy thông tin giao dịch!");
                jButton8.setEnabled(false);
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi khi tải thông tin chi tiết: " + e.getMessage());
            jButton8.setEnabled(false);
        }
  }

