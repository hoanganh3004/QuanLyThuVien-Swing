   private void loadNotifications() {
    System.out.println("Starting loadNotifications...");
    DefaultTableModel model = (DefaultTableModel) jTable3.getModel();
    model.setRowCount(0);

    try {
        // Ghi log trước khi thực thi truy vấn
        System.out.println("Executing SQL query...");
        // Truy vấn lấy thông báo mượn và trả sách
        String sql = "SELECT message, created_at " +
                     "FROM notifications " +
                     "WHERE user_id = 1 AND (message LIKE ? OR message LIKE ?) " +
                     "ORDER BY created_at DESC";
        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setString(1, "%đã mượn sách%");
        ps.setString(2, "%đã trả sách%");
        ResultSet rs = ps.executeQuery();

        int rowCount = 0;
        // Duyệt qua kết quả
        while (rs.next()) {
            rowCount++;
            String message = rs.getString("message");
            // Xử lý null cho created_at
            String createdAt = rs.getTimestamp("created_at") != null ? rs.getTimestamp("created_at").toString() : "";
            model.addRow(new Object[]{message, createdAt});
        }
        // Ghi log số dòng tải được
        System.out.println("Total rows loaded: " + rowCount);
        // Thông báo nếu không có thông báo
        if (rowCount == 0) {
            JOptionPane.showMessageDialog(this, "Không có thông báo nào để hiển thị!");
        }
    } catch (SQLException e) {
        // Ghi log lỗi
        e.printStackTrace();
        System.out.println("SQL Error: " + e.getMessage());
        // Hiển thị thông báo lỗi
        JOptionPane.showMessageDialog(this, "Lỗi khi tải danh sách thông báo: " + e.getMessage());
    }

    // Cập nhật giao diện bảng
    jTable3.revalidate();
    jTable3.repaint();
   }

