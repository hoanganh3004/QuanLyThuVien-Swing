    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        String monthStr = jComboBox1.getSelectedItem().toString();
        // Xóa "Tháng " để lấy số
        monthStr = monthStr.replace("Tháng ", "").trim();
        int month;
        try {
            month = Integer.parseInt(monthStr);
        } catch (NumberFormatException e) {
            // Thông báo nếu tháng không hợp lệ
            JOptionPane.showMessageDialog(this, "Tháng không hợp lệ!");
            return;
        }
        // Lấy năm từ jComboBox2
        int year = Integer.parseInt(jComboBox2.getSelectedItem().toString());

        // Lấy model của bảng jTable1
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        // Xóa dữ liệu cũ
        model.setRowCount(0);

        try {
            // Truy vấn thống kê lượt mượn sách
            String sql = "SELECT b.title, s.borrow_count " +
                         "FROM statistics s " +
                         "JOIN books b ON s.book_id = b.id " +
                         "WHERE s.month = ? AND s.year = ? " +
                         "ORDER BY s.borrow_count DESC";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setInt(1, month);
            ps.setInt(2, year);
            ResultSet rs = ps.executeQuery();

            // Thêm dữ liệu vào bảng
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("title"),
                    rs.getInt("borrow_count")
                });
            }

            // Thông báo nếu không có dữ liệu
            if (model.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "Không có dữ liệu thống kê cho tháng " + month + " năm " + year);
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi khi thống kê: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton6ActionPerformed

