    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
       // Lấy từ khóa tìm kiếm từ jTextField1
       String searchName = jTextField1.getText().trim();
       // Nếu từ khóa rỗng, tải toàn bộ thông báo
       if (searchName.isEmpty()) {
           loadNotifications();
           return;
       }

       // Lấy model của bảng jTable3
       DefaultTableModel model = (DefaultTableModel) jTable3.getModel();
       // Xóa dữ liệu cũ
       model.setRowCount(0);

       try {
           // Truy vấn thông báo theo tên người dùng
           String sql = "SELECT u.full_name, u.email, n.message, n.created_at " +
                        "FROM notifications n " +
                        "JOIN users u ON n.user_id = u.id " +
                        "WHERE u.full_name LIKE ? " +
                        "ORDER BY n.created_at DESC";
           PreparedStatement ps = conn.prepareStatement(sql);
           ps.setString(1, "%" + searchName + "%");
           ResultSet rs = ps.executeQuery();

           int rowCount = 0;
           // Thêm dữ liệu vào bảng
           while (rs.next()) {
               rowCount++;
               String fullName = rs.getString("full_name") != null ? rs.getString("full_name") : "";
               String email = rs.getString("email") != null ? rs.getString("email") : "";
               String message = rs.getString("message");
               String createdAt = rs.getString("created_at");
               model.addRow(new Object[]{fullName, email, message, createdAt});
           }

           // Thông báo kết quả tìm kiếm
           if (rowCount == 0) {
               JOptionPane.showMessageDialog(this, "Không tìm thấy thông báo cho: " + searchName);
           } else {
               JOptionPane.showMessageDialog(this, "Đã tìm thấy " + rowCount + " thông báo!");
           }
       } catch (SQLException e) {
           // Ghi log lỗi
           e.printStackTrace();
           // Hiển thị thông báo lỗi
           JOptionPane.showMessageDialog(this, "Lỗi khi tìm kiếm: " + e.getMessage());
       }
    }//GEN-LAST:event_jButton9ActionPerformed

