    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
       String fullName = tfFullName.getText();
        String email = tfEmail.getText();
        String password = new String(pfPassword.getPassword());
        String confirmPassword = new String(pfConfirmPassword.getPassword());

        if (!confirmPassword.isEmpty() && !password.equals(confirmPassword)) {
            JOptionPane.showMessageDialog(this, "Mật khẩu xác nhận không khớp.");
            return;
        }

        try (Connection conn = Dbconnect.getConnection()) {
            String sql;
            if (confirmPassword.isEmpty()) {
                sql = "UPDATE users SET full_name=?, email=? WHERE username=?";
                PreparedStatement ps = conn.prepareStatement(sql);
                ps.setString(1, fullName);
                ps.setString(2, email);
                ps.setString(3, currentUsername);
                ps.executeUpdate();
            } else {
                String hashedPassword = hashPassword(password);
                sql = "UPDATE users SET full_name=?, email=?, password=? WHERE username=?";
                PreparedStatement ps = conn.prepareStatement(sql);
                ps.setString(1, fullName);
                ps.setString(2, email);
                ps.setString(3, hashedPassword);
                ps.setString(4, currentUsername);
                ps.executeUpdate();
            }

            JOptionPane.showMessageDialog(this, "Cập nhật thông tin thành công.");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Lỗi khi cập nhật thông tin: " + ex.getMessage());
        }
    }

    private String hashPassword(String password) {
        try {
            MessageDigest md = MessageDigest.getInstance("SHA-256");
            byte[] hashedBytes = md.digest(password.getBytes("UTF-8"));
            StringBuilder sb = new StringBuilder();
            for (byte b : hashedBytes) {
                sb.append(String.format("%02x", b));
            }
            return sb.toString();
        } catch (Exception ex) {
            ex.printStackTrace();
            return null;
        }
    }//GEN-LAST:event_btnSaveActionPerformed

